#include "ft_printf.h"

int	ft_putnbr(int n, int res)
{
	if (n != -2147483648)
	{
		if (n < 0)
		{
			write(1, "-", 1);
			res++;
			n = -n;
		}
		if (n >= 10)
			res = ft_putnbr(n / 10, res);
		n %= 10;
		if (n < 10)
		{
			n += '0';
			write(1, &n, 1);
			res++;
		}
	}
	else
	{
		write(1, "-2147483648", 11);
		res += 11;
	}
	return (res);
}
