#include "libftprintf.h"

void	ft_putnbr(int n)
{
	int	a;

	if (n != -2147483648)
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			n = n % 10;
		}
		if (n < 10)
		{
			a = n + '0';
			write(1, &a, 1);
		}
	}
	else
		write(1, "-2147483648", 11);
}
